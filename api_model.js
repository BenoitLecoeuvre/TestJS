class UserModel {
    constructor(firstname, lastname, adress, email, birthday, image, tel, password) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.adress = adress;
        this.email = email;
        this.birthday = birthday;
        this.image = image;
        this.tel = tel;
        this.password = password;
    }
}

export {UserModel};